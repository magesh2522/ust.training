#include <iostream>
using namespace std;


int main() {

	int n;
	cout << "Enter the number:" << endl;
	cin >> n;

	if (n < 0) {
		cout << "Negative Number" << endl;
	}
	else if(n > 0)
	{
		cout << "Postive Number" << endl;
	}
}
