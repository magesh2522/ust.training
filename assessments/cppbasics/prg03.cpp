#include <iostream>
using namespace std;


int main() {
	int n;
	cout << "Check the Number Odd or even:" << endl;
	cin >> n;

	if (n%2==0) {
		cout << "This is Even Number" << endl;
	}
	else {
		cout << "this is Odd Number" << endl;
	}
	return 0;
}
